def atbash_decrypt(ciphertext):
    decrypted = []
    for char in ciphertext.upper():
        if char.isalpha():
            # Convert char to Atbash by reversing A-Z
            decrypted.append(chr(ord('Z') - (ord(char) - ord('A'))))
        else:
            decrypted.append(char)
    return ''.join(decrypted)

LETTERS = 'ABCDEFGHIJKLMNOPQRSTUVWXYZ'  # Define LETTERS for getLetterCount function

def getLetterCount(message):
    letterCount = {letter: 0 for letter in LETTERS}
    for letter in message.upper():
        if letter in LETTERS:
            letterCount[letter] += 1
    return letterCount

